function productNumbers(array){
    let array2 = []
    for (let i = 0; i < array.length; i++) {
        let number = 1
        for (let j = 0; j < array.length; j++) {
            if(i !== j){
                number *= array[j]
            }
        }
        array2.push(number)
    }
    return array2
}

array = [1, 2, 3, 4, 5]
console.log(productNumbers(array));
